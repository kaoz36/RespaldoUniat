package com.jaguarlabs.sipaccel.vo;

public class PromotoriaVO {

	private String idPromotoria;
	private String promot;
	private String razonSocial;
	private int mostrar_agte;
	
	public PromotoriaVO() {}

	public String getIdPromotoria() {
		return idPromotoria;
	}

	public void setIdPromotoria(String idPromotoria) {
		this.idPromotoria = idPromotoria;
	}

	public String getPromot() {
		return promot;
	}

	public void setPromot(String promot) {
		this.promot = promot;
	}

	public String getRazonSocial() {
		return razonSocial;
	}

	public void setRazonSocial(String razonSocial) {
		this.razonSocial = razonSocial;
	}

	public int getMostrar_agte() {
		return mostrar_agte;
	}

	public void setMostrar_agte(int mostrar_agte) {
		this.mostrar_agte = mostrar_agte;
	}
	
	
	
	
}
